    private void eventDSBH() {
        lammoiBT.addActionListener(e -> {
            manvCB.removeAllItems();
            dichvuCB.removeAllItems();
            for (NhanVien i : listNV) {
                manvCB.addItem(i.getMa() + "");
            }
            for (DichVu i : listDV) {
                dichvuCB.addItem(i.getMa() + "");
            }
        });
        themmoiBT.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                try {
                    String manv = manvCB.getSelectedItem().toString();
                    String madv = dichvuCB.getSelectedItem().toString();
                    int so = Integer.parseInt(soluong.getText());
                    DSBH a = new DSBH(getNhanVien(Integer.parseInt(manv)), getDichVu(Integer.parseInt(madv)), so);
                    listDSBH.add(a);
                    tmDS.addRow(a.toObject());
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(Main.this, "Nhap so");
                    return;
                }
            }
        });
        luufileBT.addActionListener(e ->{
            IOFile.viet(fDS, listDSBH);
        });
        
        sapxepBT.addActionListener(e ->{
            if (sapxepCB.getSelectedIndex() == 0) {
                listDSBH.sort(new Comparator<DSBH>() {
                    @Override
                    public int compare(DSBH o1, DSBH o2) {
                        String ten1 = o1.getNv().getHoten(), ten2 = o2.getNv().getHoten();
                        String[] a = ten1.split("\\s+"), b = ten2.split("\\s+");
                        ten1 = a[a.length - 1] + o1.getNv().getHoten();
                        ten2 = b[b.length - 1] + o2.getNv().getHoten();
                        return ten1.compareTo(ten2);
                    }
                });
            } else {
                listDSBH.sort(new Comparator<DSBH>() {
                    @Override
                    public int compare(DSBH o1, DSBH o2) {
                        String ten1 = o1.getDv().getTen(), ten2 = o2.getDv().getTen();
                        String[] a = ten1.split("\\s+"), b = ten2.split("\\s+");
                        ten1 = a[a.length - 1] + o1.getDv().getTen();
                        ten2 = b[b.length - 1] + o2.getDv().getTen();
                        return ten1.compareTo(ten2);
                    }
                });
            }
            showDS(listDSBH);
        });
    }
