    public void loadDS() {
        File f = new File(fDS);
        if (f.exists()) {
            listDSBH = IOFile.doc(fDS);
        } else {
            listDSBH = new ArrayList();
        }
    }
    private void loadDV() {
        File f = new File(fDV);
        if (f.exists()) {
            listDV = IOFile.doc(fDV);
        } else {
            listDV = new ArrayList<>();
        }
    }
    private void loadNV() {
        File f = new File(fDV);
        if (f.exists()) {
            listNV = IOFile.doc(fNV);
        } else {
            listNV = new ArrayList<>();
        }
    }
    private void showDV(List<DichVu> lst) {
        tmDV.setRowCount(0);
        for (DichVu i : lst) 
            tmDV.addRow(i.toObjects());
    }
    private void showNV(List<NhanVien> lst) {
        tmNV.setRowCount(0);
        for (NhanVien i : lst) 
            tmNV.addRow(i.toObjects());
    }
    private void showDS(List<DSBH> list) {
        tmDS.setRowCount(0);
        for (DSBH i : list) {
            tmDS.addRow(i.toObject());
        }
    }
    
    private void timKiem() {
        btTim.addActionListener(e -> {
            String key = jTextField9.getText();
            List<NhanVien> lst = new ArrayList<>();
            for (NhanVien i : listNV) {
                if (i.getHoten().contains(key.toLowerCase())) lst.add(i);
            }
            showNV(lst);
        });
    }

